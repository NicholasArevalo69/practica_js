////////////////////////OPERADORES
let numero1 = 7;
let numero2 = 69;
/////////////////////////////* ARITMETICOS */
//suma
console.log(numero1 + numero2);

//resta
console.log(numero1 - numero2);

//multiplicacion
console.log(numero1 * numero2);

//division
console.log(numero1 / numero2);

//modulo devuelve el residuo de una division
console.log(numero1 % numero2);

//incremento
console.log(numero1++);

//decremento
console.log(numero1--);

//////////////////////////*OPERADORES ASIGNACION*/
//asignacion nuevo valor a numero1
numero1 = 105;

////////////////////////OPERADORES UNARIOS
//incremento
numero1++;
//decremento
numero1--;

////////////////////////OPERADORES COMPARACION
//doble igual compara si valor numero1 es igual a 99
console.log(numero1 == 99);

//triple igual compara si el tipo y valor numero1 es igual a tipo y valor 99
console.log(numero1 === 99);

//diferente de, verifica si un valor es diferente de otro
console.log(numero1 != 99);

//diferente de doble, verifica si un valor y el tipo es diferente de otro
console.log(numero1 !== 99);
